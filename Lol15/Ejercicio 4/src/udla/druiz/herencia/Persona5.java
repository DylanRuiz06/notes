package udla.druiz.herencia;

public class Persona5 {
    private String nombree;
    private String lastnombree;
    private int edadd;
    private String maill;

    public Persona5(String nombree, String lastnombree) {
    }


    public String getNombree() {
        return nombree;
    }

    public void setNombree(String nombree) {
        this.nombree = nombree;
    }

    public String getLastnombree() {
        return lastnombree;
    }

    public void setLastnombree(String lastnombree) {
        this.lastnombree = lastnombree;
    }

    public int getEdadd() {
        return edadd;
    }

    public void setEdadd(int edadd) {
        this.edadd = edadd;
    }

    public String getMaill() {
        return maill;
    }

    public void setMaill(String maill) {
        this.maill = maill;
    }
}
